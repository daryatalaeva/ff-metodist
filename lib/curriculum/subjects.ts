/**
 * Ищет предмет по введённому пользователем названию (регистронезависимо).
 * Возвращает SubjectInfo или null, если предмет не найден.
 */
export function findSubject(input: string): SubjectInfo | null {
  const normalized = input.trim().toLowerCase()
  if (!normalized) return null

  return (
    SUBJECTS.find((s) =>
      s.aliases.some(
        (a) =>
          normalized === a ||
          normalized.includes(a) ||
          a.includes(normalized),
      ),
    ) ?? null
  )
}
